package com.jnwan.worldcuponplam.model;

public class GameTime

{

    public int day;

    public int hour;

    public int minute;

    public int month;

    

    public GameTime(final int month, final int day, final int hour, final int minute) {

        super();

        this.month = month;

        this.day = day;

        this.hour = hour;

        this.minute = minute;

    }

}